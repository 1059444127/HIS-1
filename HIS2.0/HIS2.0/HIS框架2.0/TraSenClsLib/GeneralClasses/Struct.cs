		/// 小时
		/// </summary>
		public short wHour; 
		/// <summary>
		/// 分
		/// </summary>
		public short wMinute; 
		/// <summary>
		/// 秒
		/// </summary>
		public short wSecond; 
		/// <summary>
		/// 毫秒
